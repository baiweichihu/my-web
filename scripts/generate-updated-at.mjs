import { execFileSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const thisFile = fileURLToPath(import.meta.url);

const trackedFiles = {
  hero: 'src/hero/heroData.js',
  sections: {
    about: 'src/sections/AboutSection.jsx',
    interests: 'src/sections/InterestsSection.jsx',
    study: 'src/sections/StudySection.jsx',
  },
};

function today() {
  const parts = new Intl.DateTimeFormat('en-CA', {
    timeZone: 'Asia/Shanghai',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
  }).formatToParts(new Date());
  const values = Object.fromEntries(parts.map((part) => [part.type, part.value]));
  return `${values.year}-${values.month}-${values.day}`;
}

function gitDateFor(filePath) {
  try {
    return execFileSync('git', ['log', '-1', '--format=%cs', '--', filePath], {
      cwd: rootDir,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).trim();
  } catch {
    return '';
  }
}

function resolveDate(filePath) {
  return gitDateFor(filePath) || today();
}

export function generateUpdatedAt() {
  const updatedAt = {
    hero: resolveDate(trackedFiles.hero),
    sections: Object.fromEntries(
      Object.entries(trackedFiles.sections).map(([sectionId, filePath]) => [sectionId, resolveDate(filePath)]),
    ),
  };

  const generatedDir = path.join(rootDir, 'src', 'generated');
  mkdirSync(generatedDir, { recursive: true });

  writeFileSync(
    path.join(generatedDir, 'updatedAt.js'),
    `// This file is generated by scripts/generate-updated-at.mjs.\nexport const generatedUpdatedAt = ${JSON.stringify(updatedAt, null, 2)};\n`,
  );

  return updatedAt;
}

if (process.argv[1] && path.resolve(process.argv[1]) === thisFile) {
  generateUpdatedAt();
}
